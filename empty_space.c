int		empty_space(char *tetro)
{
	int i;

	i = 0;
	if (tetro[0] < 'A' && tetro[1] >= 'A')
		i = 1;
	else if (tetro[0] < 'A' && tetro[1] < 'A')
		i = 2;
	return (i);
}
